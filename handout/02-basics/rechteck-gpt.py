# rechteck.py

# Variablen für Länge und Breite mit Defaultwerten initialisieren
länge = 10
breite = 5

# Umfang und Fläche berechnen
umfang = 2 * (länge + breite)
fläche = länge * breite

# Ergebnisse auf der Konsole ausgeben
print(f"Rechteck mit Länge {länge} und Breite {breite}")
print(f"- Umfang = {umfang}")
print(f"- Fläche = {fläche}")
